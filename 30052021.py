#Program to write my name that stored in a variable and printing it on console including reversing it.

#storing my name in 'name' variable
name = "Vishnu"
#printing it on console
print(name)
#slicing the string to make it in reverse order. 
#we know that -1 is the index of last character of the string.
#so here we are printing it from start to end in step of -1 (means last)
print(name[::-1])
